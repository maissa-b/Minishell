#include <libft_display.h>

void		ft_aff_tabi(int **tab)
{
	int i;
	int	j;

	i = 0;
	j = 0;
	while (tab[i])
	{
		while (tab[i][j])
		{
			ft_putnbr(tab[i][j]);
			j++;
		}
		i++;
	}
}
