#include <libft_travel.h>

int		ft_ispunct(int c)
{
	return (((c >= 041 && c <= 57) || (c >= 072 && c <= 100) || \
	(c >= 133 && c <= 140) || (c >= 173 && c <= 176)) ? 1 : 0);
}
